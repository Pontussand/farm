
public class Cow {

    private int bornYear;
    private String name;

    public Cow(int bornYear, String name) {
        this.bornYear = bornYear;
        this.name = name;
    }

    public String getName() {
        return this.name;
    }

    public int getBirthYear() {
        return this.bornYear;
    }


}
